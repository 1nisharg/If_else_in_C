#include <stdio.h>

int main()
{
    int var=2;

    switch(var){
        case 1:
        printf("case 1 is executed");
        break;
        case 2:
        printf("case 2 is executed");
        break;
        case 3:
        printf("case 3 is executed");
        break;
        default:
        printf("default case is executed!!");
        break;
    }

return 0;

}